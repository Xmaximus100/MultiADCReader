AT_StatusTypeDef USB_Write(const char* buf, size_t len){
    size_t n = RingBuffer_WriteString(&rb_tx, buf, len);
    uint32_t packets = 0;
    //Poking TxPump to start transmission
    while (USB_TxPumpFromRing(&rb_tx) != 0) packets++;
    return (n == len) ? AT_OK : AT_ERROR;
}

